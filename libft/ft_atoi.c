int	ft_atoi(const char *str)
{
	long int	a;
	int			b;
	int			k;

	k = 1;
	b = 0;
	a = 0;
	while (*(str + b) == '\t' || *(str + b) == '\n' || *(str + b) == ' '
			|| *(str + b) == '\v' || *(str + b) == '\f' || *(str + b) == '\r')
		b++;
	if (*(str + b) == '+' || *(str + b) == '-')
	{
		if (*(str + b) == '-')
			k = k * -1;
		b++;
	}
	while (*(str + b) && *(str + b) >= '0' && *(str + b) <= '9')
	{
		a = a * 10 + (*(str + b) - '0');
		b++;
	}
	return (k * a);
}
