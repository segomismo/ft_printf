#include <string.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned int	a;
	unsigned char	*b;

	b = s;
	a = 0;
	while (a < n && b[a])
	{
		b[a] = '\0';
		a++;
	}
	s = b;
}
